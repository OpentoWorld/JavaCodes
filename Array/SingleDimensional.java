package Array;

class SingleDimensional {
    public static void main(String[] args) {
        int a[]=new int[]{1,2,3,4,5,6};
        System.out.println("Array elements are-------------------");
        for (int i:a) {
            System.out.println(i);
        }
        System.out.println("Array length:"+a.length);
    }
}

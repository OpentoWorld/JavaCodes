class DataTypesAndVariable {
    public static void main(String[] args) {
        byte b=10;
        short s=20;
        int i=10;
        long l=40l;
        float f=30.5f;
        double d=13.55555d;
        char c='a';
        boolean bl=false;
        String str="Hello";
        System.out.println("Byte:"+b+"\nShort:"+s+"\nInteger:"+i+"\nLong:"+l+"\nFloat:"+f+"\nDouble:"+d+"\nCharacter:"+c+"\nBoolean:"+bl+"\nString:"+str);
    }
}
